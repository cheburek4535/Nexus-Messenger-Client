type Language = 'en' | 'ru';

let currentLanguage: Language = 'en';
let translations: Record<string, LangDict[keyof LangDict]> = en;
let listeners: Array<() => void> = [];

export function t(key: string, ...args: string[]): string {
  const val = translations[key];
  if (val === undefined) return key;
  if (typeof val === 'function') {
    return (val as (...args: string[]) => string)(...args);
  }
  let result = val as string;
  if (args.length > 0) {
    result = result.replace(/\{(\d+)\}/g, (_, idx) => args[parseInt(idx)] ?? '');
    result = result.replace(/\{(\w+)\}/g, (_, name) => args[0] ?? '');
  }
  return result;
}

export function getCurrentLanguage(): Language {
  return currentLanguage;
}

export function setLanguage(lang: Language): void {
  currentLanguage = lang;
  translations = lang === 'ru' ? ru : en;
  getDatabase().then(db => {
    db.runAsync(`INSERT OR REPLACE INTO settings (key, value, updated_at) VALUES ('language', ?, ?)`, [lang, Date.now()]);
  }).catch(() => {});
  listeners.forEach(fn => fn());
}

export function onLanguageChange(fn: () => void): () => void {
  listeners.push(fn);
  return () => {
    listeners = listeners.filter(f => f !== fn);
  };
}

export async function loadSavedLanguage(): Promise<Language> {
  try {
    const db = await getDatabase();
    const row = await db.getFirstAsync<{ value: string }>(`SELECT value FROM settings WHERE key = 'language'`);
    if (row && (row.value === 'ru' || row.value === 'en')) {
      currentLanguage = row.value as Language;
      translations = row.value === 'ru' ? ru : en;
    }
  } catch {}
  return currentLanguage;
}
